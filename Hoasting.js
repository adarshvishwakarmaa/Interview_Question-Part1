// diffrent betwween let and var

//1. Let
// let is block-scoped.
// let does not allow to redeclare variables.
// let is a feature of ES6.
// Hoisting is not allowed with let.

//2 . Var
// var is function scoped.
// var allows to redeclare variables.
// var is an ECMAScript1 feature.
// Hoisting is allowed with var.




//3.Hoisting
// In Hoisting a variable can be used before it has been declared.


